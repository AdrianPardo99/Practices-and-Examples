typedef int Elem;
/*Funcion que compara menor o igual*/
Elem EsMoI(Elem e,Elem e1){return e<=e1;}
/*Función que imprime cada elemento*/
void impElem(Elem e){printf("%d\n",e);}
/*Función que compara es mayor*/
Elem EsMayor(Elem e,Elem e1){return e>e1;}
/*Función que compara mayor o igual*/
Elem EsMAoI(Elem e,Elem e1){return e>=e1;}
/*Función que imprime cada elemento para los conjuntos*/
void impCon(Elem e){printf("%d",e);}
/*Función de comparación igual*/
int esIgual(Elem e,Elem e1){return e==e1;}
